const countOnly = function(array, counts) {
  // Iterate through counts elements
  //  if element.value is false
  //    do nothing
  //  else
  //    nest iterate through array
  //      if array[index] === element.key
  //        add to sum
  //  return sum
  const results = {}

  for (const item of array) {
    console.log(item);
    if (counts[item]) {
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }
  return results;
};




